#include <iostream>
using namespace std;

int main() {
	double a, b;
	cout << "Please input two real number separated by spaces:" << endl;
	cin >> a >> b;
	cout << a << " + " << b << " = " << a + b << endl;
	cout << a << " - " << b << " = " << a - b << endl;
	cout << a << " * " << b << " = " << a * b << endl;
	cout << a << " / " << b << " = " << a / b << endl;
	return 0;
}